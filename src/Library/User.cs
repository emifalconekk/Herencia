namespace Library;

public class User
{
    private string name;
    private int id;
    private string familyname;
    private int calification;

    public string Name
    {
        get { return name; }
        set { Name = value; }
    }

    public int Id
    {
        get { return id; }
        set { id = value; }
    }

    public string FamilyName
    {
        get { return familyname; }
        set { familyname = value; }
    }

    public int Calification
    {
        get { return calification; }
        set { calification = value; }
    }

}